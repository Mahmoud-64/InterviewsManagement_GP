    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $level = Level::findOrfail($id);
        if($level->delete()){
            return  new LevelResource($level);
        }
        return "Error while deleting";
    }
